package edu.kit.informatik.structure.states;

/**
 * The enum Game state.
 */
public enum GameState {
    /**
     * Init game state.
     */
    INIT,
    /**
     * Shuffle game state.
     */
    SHUFFLE,
    /**
     * Runaturn game state.
     */
    RUNATURN,
    /**
     * Lost game state.
     */
    LOST,
    /**
     * Monsterturnone game state.
     */
    MONSTERTURNONE,
    /**
     * Monsterturntwo game state.
     */
    MONSTERTURNTWO,
    /**
     * Fightwon game state.
     */
    FIGHTWON,
    /**
     * Healing game state.
     */
    HEALING,

    /**
     * Runabossfight game state.
     */
    RUNABOSSFIGHT,

    /**
     * Monsterbossfight game state.
     */
    MONSTERBOSSFIGHT,

    /**
     * Bosswin game state.
     */
    BOSSWIN,

    /**
     * Win game state.
     */
    WIN
}
